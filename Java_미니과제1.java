public class Java_미니과제1 {
        public static void main(String[] args) {
            System.out.println("[구구단 출력]");
            for (int i = 1; i < 10; i++) {
                for (int j = 1; j < 10; j++) {
                    System.out.println( "0" + i + " X " + "0" + j + " = " + i*j);
                }
                System.out.println();

            }
        }

}
